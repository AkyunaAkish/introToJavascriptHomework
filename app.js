// The mathematical operator '+' is how javascript adds two values together
var addition = 3 + 3;
console.log(addition);


// The mathematical operator '-' is how javascript subtracts one value from another
var subtraction = 2 - 2;
console.log(subtraction);



// The mathematical operator '*' is how javascript multiplies two values
var multiplication = 2 * 6;
console.log(multiplication);


// The mathematical operator '/' is how javascript divides two values
var division = 2 / 6;
console.log(division);


// The mathematical operator '%' is how javascript divides two values and returns the remainder
var mod = 12 % 5;
console.log(mod);





// Math.round rounds to nearest integer
var x = Math.round(12.6);
console.log(x);


// Math.random returns random number between 0 and 1
var y = Math.random();
console.log(y);


// Math.min returns lowest number
var minimum = Math.min(1,5,8);
console.log(minimum);


// Math.max returns highest number
var maximum = Math.max(1,5,8);
console.log(maximum);


// Math.floor rounds the number to the lower integer
var z = Math.floor(3.8);
console.log(z);


// Math.ceil returns the lowest number that is greater or equal to the given number
var top = Math.ceil(3.1);
console.log(top);
