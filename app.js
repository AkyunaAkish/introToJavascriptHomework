// The mathematical operator '+' is how javascript adds two values
var additionExample = 4 + 2;
console.log(additionExample);

//The mathematical operator '-' is how javascript subtracts values
var subtractionExample = 4 - 1;
console.log(subtractionExample);

//The mathematical operator '/' is how javascript divides values
var divisionExample = 10 / 2;
console.log(divisionExample);

//The mathematical operator '*' is how javascript multiplies values
var multiplicationExample = 10 * 10;
console.log(multiplicationExample);

//The mathematical operator '%' is how javascript finds the remainder after one value is divided by another value
var moduloExample = 10 % 3;
console.log(moduloExample);

//The mathematical operator '++' is how javascript increments values
var x = 10;
var incrementExample = ++x;
console.log(incrementExample);

//The mathematical operator '--' is how javascript decrements values
var x = 10;
var decrementExample = --x;
console.log(decrementExample);

//The mathematical operator '-x' is how javascript does negation
var x = 10;
var unaryNegationExample = -x;
console.log(unaryNegationExample);

//The mathematical operator '+x' is how javascript converts values into numbers
var x = "10";
var unaryPlusExample = +x;
console.log(unaryPlusExample);

//The Math.round() function returns the given value rounded to the closest integer
var roundExample = Math.round(14.14);
console.log(roundExample);

//The Math.random() function returns a random number from 0 (inclusive) and 1 (exclusive)
var randomExample = Math.random();
console.log(randomExample);

//The Math.min() function returns the smallest of 0+ numbers
var a = 1;
var b = 10;
var minExample = Math.min(a, b);
console.log(minExample);

//The Math.max() function returns the largest of 0+ numbers
var a = 1;
var b = 10;
var maxExample = Math.max(a, b);
console.log(maxExample);

//The Math.floor() function returns the largest integer that is <= a given number
var floorExample = Math.floor(14.60);
console.log(floorExample);

//The Math.ceil() function returns the smallest integer that is >= a given number
var ceilExample = Math.ceil(14.60);
console.log(ceilExample);
