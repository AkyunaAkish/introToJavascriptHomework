var b = 10;
var c = a + b;

console.log(c);

// Operator '+' adds to values together (when used with numbers)
var addEx = 3 + 4;
console.log(addEx);

// Operator '-' subtracts the second value from the first
var subEx = 8-5;
console.log(subEx);

// Operator '/' divides the first value by the second
var divEx =32/16;
console.log(divEx);

// Operator '*' multiplies two values together
var multEx = 4*5;
console.log(multEx);

// Operator '%' returns the remainder that... remains when the first value is divided by the second
var modEx = 33%6;
console.log(modEx);

//Operator '++' increases the value by 1
var x = 4;
var incrEx = ++x;
console.log(incrEx);

//Operator '--' decreases the value by 1
var y = 14;
var decrEx = --y;
console.log(decrEx);

//Operator '-', when preceding a single value, negates it. (Turns it from positive to negative or vice versa.)
var z = 5;
var unaEx = -z;
console.log(unaEx);

//Operator '+' when preceding a string or boolean, attempts to convert the value to a number
var string ="45";
var unaplusEx = +string;
console.log(unaplusEx);

