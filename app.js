// The mathematical operator '+' is how javascript adds two values together
var a = 22;
var b = 10;
var c = a + b;

console.log(c);

// operator '-' subtracts the second value from the first
var minus = 4 - 2;
console.log(minus);

// operator '/' divides the first value by the second
var divide = 20/2;
console.log(divide);

// operator '*' multiples two values
var mult = 3*3;
console.log(mult);

// operator '%' provides the remainder when value one is divided by value two
var perc = 10 % 3;
console.log(perc);

// operator '++' increments a variable by one
var incr = 3;
y = ++incr;
console.log(y);

// operator '--' decreases a variable by one
var decr = 8;
ement = --decr;
console.log(ement);

// operator '-' ahead of a variable makes it negative
var j = 7;
h = -j;
console.log(h);

// operator '+' ahead of a variable attempts to convert it to a number
var strang = "7"
console.log(+strang);

// Math.round returns a rounded number to the nearest integer
var round = 3.24159;
console.log(Math.round(round));

// Math.random returns a random number between 0 and 1
console.log(Math.random());

// Math.min returns the lowest number of a set of values
var newVar = 1;
var anotherVar = 7;
console.log(Math.min(newVar, anotherVar));

// Math.max returns the largest number of a set of values
console.log(Math.max(newVar, anotherVar));

// Math.floor returns the integer less than or equal to a value
var hi = 39.993;
console.log(Math.floor(hi));

// Math.ceil returns the integer greater than or equal to a value
console.log(Math.ceil(hi));
